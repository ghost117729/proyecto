package modelo;

public class EquipoMilitar {
	private int idequipomilitar;
	private String tipodeCasco;
	private String tipodeChaleco;
	private String tipodeBotas;
	private String tipodeUniforme;
	
	
	public int getIdequipomilitar() {
		return idequipomilitar;
	}
	public void setIdequipomilitar(int idequipomilitar) {
		this.idequipomilitar = idequipomilitar;
	}
	public String getTipodeCasco() {
		return tipodeCasco;
	}
	public void setTipodeCasco(String tipodeCasco) {
		this.tipodeCasco = tipodeCasco;
	}
	public String getTipodeChaleco() {
		return tipodeChaleco;
	}
	public void setTipodeChaleco(String tipodeChaleco) {
		this.tipodeChaleco = tipodeChaleco;
	}
	public String getTipodeBotas() {
		return tipodeBotas;
	}
	public void setTipodeBotas(String tipodeBotas) {
		this.tipodeBotas = tipodeBotas;
	}
	public String getTipodeUniforme() {
		return tipodeUniforme;
	}
	public void setTipodeUniforme(String tipodeUniforme) {
		this.tipodeUniforme = tipodeUniforme;
	}
	

}
