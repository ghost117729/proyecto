package modelo;

public class Oficiales {
	private int idoficial;
	private String nombreOficial;
	private String rangodeOficial;
	private String ramaMilitar;

	
	public int getIdoficial() {
		return idoficial;
	}
	public void setIdoficial(int idoficial) {
		this.idoficial = idoficial;
	}
	public String getNombreOficial() {
		return nombreOficial;
	}
	public void setNombreOficial(String nombreOficial) {
		this.nombreOficial = nombreOficial;
	}
	public String getRangodeOficial() {
		return rangodeOficial;
	}
	public void setRangodeOficial(String rangodeOficial) {
		this.rangodeOficial = rangodeOficial;
	}
	public String getRamaMilitar() {
		return ramaMilitar;
	}
	public void setRamaMilitar(String ramaMilitar) {
		this.ramaMilitar = ramaMilitar;
	}

	
}
