package modelo;

public class Cursosmilitares {
	private int idCursos;
	private String nombredeCurso;
	private String duraciondelCurso;
	
	public int getIdCursos() {
		return idCursos;
	}
	public void setIdCursos(int idCursos) {
		this.idCursos = idCursos;
	}
	public String getNombredeCurso() {
		return nombredeCurso;
	}
	public void setNombredeCurso(String nombredeCurso) {
		this.nombredeCurso = nombredeCurso;
	}
	public String getDuraciondelCurso() {
		return duraciondelCurso;
	}
	public void setDuraciondelCurso(String duraciondelCurso) {
		this.duraciondelCurso = duraciondelCurso;
	}

}
