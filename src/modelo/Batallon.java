package modelo;

public class Batallon {
	private int idbatallon;
	private String nombredelBatallon;
	private String zonaMilitar;
	private int numerodeElementos;
	private int numerodeVehiculos;
	private String especialidad;
	
	
	public int getIdbatallon() {
		return idbatallon;
	}
	public void setIdbatallon(int idbatallon) {
		this.idbatallon = idbatallon;
	}
	public String getNombredelBatallon() {
		return nombredelBatallon;
	}
	public void setNombredelBatallon(String nombredelBatallon) {
		this.nombredelBatallon = nombredelBatallon;
	}
	public String getZonaMilitar() {
		return zonaMilitar;
	}
	public void setZonaMilitar(String zonaMilitar) {
		this.zonaMilitar = zonaMilitar;
	}
	public int getNumerodeElementos() {
		return numerodeElementos;
	}
	public void setNumerodeElementos(int numerodeElementos) {
		this.numerodeElementos = numerodeElementos;
	}
	public int getNumerodeVehiculos() {
		return numerodeVehiculos;
	}
	public void setNumerodeVehiculos(int numerodeVehiculos) {
		this.numerodeVehiculos = numerodeVehiculos;
	}
	public String getEspecialidad() {
		return especialidad;
	}
	public void setEspecialidad(String especialidad) {
		this.especialidad = especialidad;
	}
	
	

}
