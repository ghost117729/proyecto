package modelo;

public class Caballosmilitares {
	private int idcaballos;
	private String nombredecaballo;
	private String raza;
	private String especialidad;
	
	
	public int getIdcaballos() {
		return idcaballos;
	}
	public void setIdcaballos(int idcaballos) {
		this.idcaballos = idcaballos;
	}
	public String getNombredecaballo() {
		return nombredecaballo;
	}
	public void setNombredecaballo(String nombredecaballo) {
		this.nombredecaballo = nombredecaballo;
	}
	public String getRaza() {
		return raza;
	}
	public void setRaza(String raza) {
		this.raza = raza;
	}
	public String getEspecialidad() {
		return especialidad;
	}
	public void setEspecialidad(String especialidad) {
		this.especialidad = especialidad;
	}
	
	

}
