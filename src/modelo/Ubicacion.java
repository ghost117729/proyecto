package modelo;

public class Ubicacion {
	private String estado;
	private String zonaMilitar;
	private String regionMilitar;
	private int idubicacion;
	
	
	
	public int getIdubicacion() {
		return idubicacion;
	}
	public void setIdubicacion(int idubicacion) {
		this.idubicacion = idubicacion;
	}
	public String getEstado() {
		return estado;
	}
	public void setEstado(String estado) {
		this.estado = estado;
	}
	public String getZonaMilitar() {
		return zonaMilitar;
	}
	public void setZonaMilitar(String zonaMilitar) {
		this.zonaMilitar = zonaMilitar;
	}
	public String getRegionMilitar() {
		return regionMilitar;
	}
	public void setRegionMilitar(String regionMilitar) {
		this.regionMilitar = regionMilitar;
	}
	
	

}
