package modelo;

public class PerrosMilitares {
	private int idperros;
	private String nombredeperro;
	private String raza;
	private String especialidad;
	
	
	public int getIdperros() {
		return idperros;
	}
	public void setIdperros(int idperros) {
		this.idperros = idperros;
	}
	public String getNombredeperro() {
		return nombredeperro;
	}
	public void setNombredeperro(String nombredeperro) {
		this.nombredeperro = nombredeperro;
	}
	public String getRaza() {
		return raza;
	}
	public void setRaza(String raza) {
		this.raza = raza;
	}
	public String getEspecialidad() {
		return especialidad;
	}
	public void setEspecialidad(String especialidad) {
		this.especialidad = especialidad;
	}

}
