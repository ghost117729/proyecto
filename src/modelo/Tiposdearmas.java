package modelo;

public class Tiposdearmas {
	private String tipodeModelo;
	private String tipodeMunicion;
	private String tipodeAlcance;
	private String tipodeCalibre;
	private int idarmas;
	
	
	public int getIdarmas() {
		return idarmas;
	}
	public void setIdarmas(int idarmas) {
		this.idarmas = idarmas;
	}
	public String getTipodeModelo() {
		return tipodeModelo;
	}
	public void setTipodeModelo(String tipodeModelo) {
		this.tipodeModelo = tipodeModelo;
	}
	public String getTipodeMunicion() {
		return tipodeMunicion;
	}
	public void setTipodeMunicion(String tipodeMunicion) {
		this.tipodeMunicion = tipodeMunicion;
	}
	public String getTipodeAlcance() {
		return tipodeAlcance;
	}
	public void setTipodeAlcance(String tipodeAlcance) {
		this.tipodeAlcance = tipodeAlcance;
	}
	public String getTipodeCalibre() {
		return tipodeCalibre;
	}
	public void setTipodeCalibre(String tipodeCalibre) {
		this.tipodeCalibre = tipodeCalibre;
	}

	
}
