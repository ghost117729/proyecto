package modelo;

public class Escuadron {
	private String nombredeEscuadron;
	private String sargento;
	private String fusilero;
	private String granadero;
	private String ametralladora;
	private String rebastecedor;
	private String medico;
	
	
	
	public String getSargento() {
		return sargento;
	}
	public void setSargento(String sargento) {
		this.sargento = sargento;
	}
	public String getFusilero() {
		return fusilero;
	}
	public void setFusilero(String fusilero) {
		this.fusilero = fusilero;
	}
	public String getGranadero() {
		return granadero;
	}
	public void setGranadero(String granadero) {
		this.granadero = granadero;
	}
	public String getAmetralladora() {
		return ametralladora;
	}
	public void setAmetralladora(String ametralladora) {
		this.ametralladora = ametralladora;
	}
	public String getRebastecedor() {
		return rebastecedor;
	}
	public void setRebastecedor(String rebastecedor) {
		this.rebastecedor = rebastecedor;
	}
	public String getMedico() {
		return medico;
	}
	public void setMedico(String medico) {
		this.medico = medico;
	}
	public String getNombredeEscuadron() {
		return nombredeEscuadron;
	}
	public void setNombredeEscuadron(String nombredeEscuadron) {
		this.nombredeEscuadron = nombredeEscuadron;
	}

	
	
}
