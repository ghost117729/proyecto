package modelo;

public class VehiculosTerrestre {
	private String camionesMilitares;
	private String carrosdeCombate;
	private String carrosBlindaos;
	private String carrosMultiproposito;
	
	
	
	public String getCamionesMilitares() {
		return camionesMilitares;
	}
	public void setCamionesMilitares(String camionesMilitares) {
		this.camionesMilitares = camionesMilitares;
	}
	public String getCarrosdeCombate() {
		return carrosdeCombate;
	}
	public void setCarrosdeCombate(String carrosdeCombate) {
		this.carrosdeCombate = carrosdeCombate;
	}
	public String getCarrosBlindaos() {
		return carrosBlindaos;
	}
	public void setCarrosBlindaos(String carrosBlindaos) {
		this.carrosBlindaos = carrosBlindaos;
	}
	public String getCarrosMultiproposito() {
		return carrosMultiproposito;
	}
	public void setCarrosMultiproposito(String carrosMultiproposito) {
		this.carrosMultiproposito = carrosMultiproposito;
	}

	
	
}
