package modelo;

public class Soldado {
	
	private String nombre;
	private String apellidoP;
	private String apellidoM;
	private String matricula;
	private String batallon;
	private String ramaMilitar;
	private String especialidad;



public String getNombre() {
	return nombre;
}

public void setNombre(String nombre) {
	this.nombre = nombre;
}

public String getApellidoP() {
	return apellidoP;
}

public void setApellidoP(String apellidoP) {
	this.apellidoP = apellidoP;
}

public String getApellidoM() {
	return apellidoM;
}

public void setApellidoM(String apellidoM) {
	this.apellidoM = apellidoM;
}

public String getMatricula() {
	return matricula;
}

public void setMatricula(String matricula) {
	this.matricula = matricula;
}



public String getBatallon() {
	return batallon;
}

public void setBatallon(String batallon) {
	this.batallon = batallon;
}

public String getRamaMilitar() {
	return ramaMilitar;
}

public void setRamaMilitar(String ramaMilitar) {
	this.ramaMilitar = ramaMilitar;
}

public String getEspecialidad() {
	return especialidad;
}

public void setEspecialidad(String especialidad) {
	this.especialidad = especialidad;
}
 

 
}
